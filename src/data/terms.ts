import type { ControlSpec } from "../scripts/demos/types";

export interface Term {
  id: string;
  name: string;
  blurb: string;
  demo: string;
  controls?: ControlSpec[];
}

export interface Category {
  id: string;
  title: string;
  subtitle: string;
  terms: Term[];
}

/* ---- reusable control specs ---- */
const duration = (def = 0.6, max = 2): ControlSpec => ({
  kind: "range",
  key: "duration",
  label: "Duration",
  min: 0.1,
  max,
  step: 0.05,
  default: def,
  unit: "s",
});
const delay = (def = 0): ControlSpec => ({
  kind: "range",
  key: "delay",
  label: "Delay",
  min: 0,
  max: 1,
  step: 0.05,
  default: def,
  unit: "s",
});
const easing: ControlSpec = {
  kind: "select",
  key: "easing",
  label: "Easing",
  options: [
    { label: "Ease out", value: "ease-out" },
    { label: "Ease in out", value: "ease-in-out" },
    { label: "Ease in", value: "ease-in" },
    { label: "Linear", value: "linear" },
  ],
  default: "ease-out",
};
const direction = (opts: string[] = ["left", "right", "top", "bottom"]): ControlSpec => ({
  kind: "select",
  key: "direction",
  label: "Direction",
  options: opts.map((o) => ({ label: o[0].toUpperCase() + o.slice(1), value: o })),
  default: opts[0],
});
const range = (
  key: string,
  label: string,
  min: number,
  max: number,
  step: number,
  def: number,
  unit?: string
): ControlSpec => ({ kind: "range", key, label, min, max, step, default: def, unit });
const toggle = (key: string, label: string, def = false): ControlSpec => ({
  kind: "toggle",
  key,
  label,
  default: def,
});

